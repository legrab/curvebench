function mulberry32(seed) {
  return function random() {
    let t = (seed += 0x6d2b79f5);
    t = Math.imul(t ^ (t >>> 15), t | 1);
    t ^= t + Math.imul(t ^ (t >>> 7), t | 61);
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

function gaussian(random) {
  const u = Math.max(random(), Number.EPSILON);
  const v = random();
  return Math.sqrt(-2 * Math.log(u)) * Math.cos(2 * Math.PI * v);
}

function range(start, end, count) {
  return Array.from({ length: count }, (_, index) => start + ((end - start) * index) / (count - 1));
}

function make2d(def) {
  const random = mulberry32(def.seed);
  const xs = def.xValues ?? range(def.min, def.max, def.count);
  const points = xs.map((x, index) => {
    const base = def.fn(x, index);
    const noise = (def.noise ?? 0) * gaussian(random);
    return { x: round(x), y: round(base + noise) };
  });
  return {
    schemaVersion: 1,
    id: def.id,
    title: def.title,
    shortDescription: def.shortDescription,
    description: def.description,
    dimension: "2d",
    category: def.category,
    tags: def.tags,
    synthetic: true,
    sourceNote: "Generated for Curvebench. Not reference data.",
    axes: {
      x: { key: "x", label: def.xLabel, unit: def.xUnit },
      y: { key: "y", label: def.yLabel, unit: def.yUnit },
    },
    points,
    recommendedModels: def.recommendedModels,
    generation: {
      seed: def.seed,
      noiseDescription: def.noise ? "Mild deterministic measurement noise." : "No added noise.",
      formulaNote: def.formulaNote,
    },
  };
}

function make3d(def) {
  const random = mulberry32(def.seed);
  const xs = range(def.xMin, def.xMax, def.xCount);
  const ys = range(def.yMin, def.yMax, def.yCount);
  const points = [];
  for (const y of ys) {
    for (const x of xs) {
      points.push({
        x: round(x),
        y: round(y),
        z: round(def.fn(x, y) + (def.noise ?? 0) * gaussian(random)),
      });
    }
  }
  return {
    schemaVersion: 1,
    id: def.id,
    title: def.title,
    shortDescription: def.shortDescription,
    description: def.description,
    dimension: "3d",
    category: def.category,
    tags: def.tags,
    synthetic: true,
    sourceNote: "Generated for Curvebench. Not reference data.",
    axes: {
      x: { key: "x", label: def.xLabel, unit: def.xUnit },
      y: { key: "y", label: def.yLabel, unit: def.yUnit },
      z: { key: "z", label: def.zLabel, unit: def.zUnit },
    },
    points,
    recommendedModels: def.recommendedModels,
    generation: {
      seed: def.seed,
      noiseDescription: def.noise ? "Mild deterministic measurement noise." : "No added noise.",
      formulaNote: def.formulaNote,
    },
  };
}

function round(value) {
  return Number(value.toPrecision(8));
}

